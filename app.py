@app.route("/submit", methods=["POST"])
@limiter.limit("5 per minute")
@limiter.limit("30 per hour")
@limiter.limit("100 per day")
def submit():
    data = request.get_json(force=True)
    text         = data.get("text", "")
    creator_id   = data.get("creator_id")
    content_type = data.get("content_type", "text")

    if content_type not in SIGNAL_WEIGHTS:
        return jsonify({
            "error": (
                f"Unsupported content_type '{content_type}'. "
                f"Supported types: {list(SIGNAL_WEIGHTS.keys())}"
            )
        }), 400

    content_id = str(uuid.uuid4())

    llm_result  = classify_with_llm(text, content_type)
    styl_result = compute_stylometric(text)
    pred_result = compute_predictability(text)
    scoring     = combine_signals(
        llm_result["confidence"],
        styl_result["score"],
        pred_result["score"],
        content_type,
    )
    label_result = generate_label(scoring["ai_probability"])

    write_audit_entry(
        content_id, creator_id, label_result["label"],
        content_type, llm_result, styl_result, pred_result, scoring,
    )

    return jsonify({
        "content_id": content_id,
        "creator_id": creator_id,
        "content_type": content_type,
        "label": label_result["label"],
        "description": label_result["description"],
        "scoring": scoring,
        "signals": {
            "llm": {
                "label": llm_result["label"],
                "confidence": llm_result["confidence"],
                "reasoning": llm_result["reasoning"],
            },
            "stylometry": {
                "score": styl_result["score"],
                "features": styl_result["features"],
            },
            "predictability": {
                "score": pred_result["score"],
                "features": pred_result["features"],
            },
        },
    })


