def write_audit_entry(content_id, creator_id, attribution, content_type,
                      llm_result, styl_result, pred_result, scoring):
    entry = {
        "timestamp": datetime.now(timezone.utc).isoformat(),
        "content_id": content_id,
        "creator_id": creator_id,
        "content_type": content_type,
        "attribution": attribution,
        "ai_probability": scoring["ai_probability"],
        "uncertainty": scoring["uncertainty"],
        "confidence_band": scoring["confidence_band"],
        "llm_score_raw": llm_result["confidence"],
        "llm_score_cal": scoring["signals"]["llm"],
        "llm_label": llm_result["label"],
        "llm_reasoning": llm_result["reasoning"],
        "styl_score_raw": styl_result["score"],
        "styl_score_cal": scoring["signals"]["stylometry"],
        "styl_features": styl_result["features"],
        "pred_score_raw": pred_result["score"],
        "pred_score_cal": scoring["signals"]["predictability"],
        "pred_features": pred_result["features"],
    }
    with LOG_PATH.open("a") as f:
        f.write(json.dumps(entry) + "\n")


def get_log(limit=20):
    if not LOG_PATH.exists():
        return []
    entries = [json.loads(line) for line in LOG_PATH.read_text().splitlines() if line.strip()]

    appealed_ids = {e["content_id"] for e in entries if e.get("type") == "appeal"}

    annotated = []
    for e in entries:
        if "type" not in e:
            e = {**e, "appeal_filed": e["content_id"] in appealed_ids}
        annotated.append(e)

    return list(reversed(annotated))[:limit]


# ---------------------------------------------------------------------------
# Signal 1: LLM-based classification (Groq)
# ---------------------------------------------------------------------------

def classify_with_llm(text: str, content_type: str = "text") -> dict:
    """
    Signal 1: LLM-based classification via Groq.
    Measures semantic + contextual alignment with learned patterns.

    For content_type="image_description", uses IMAGE_SYSTEM_PROMPT, which is
    tuned to how AI and humans describe visual content differently.

    Returns:
        label      (str)   "AI-generated" | "Human-written"
        confidence (float) raw score — 0.0 = definitely human, 1.0 = definitely AI
        reasoning  (str)   plain-language explanation from the model
    """
    prompt = IMAGE_SYSTEM_PROMPT if content_type == "image_description" else SYSTEM_PROMPT
    response = _groq.chat.completions.create(
        model="llama-3.3-70b-versatile",
        messages=[
            {"role": "system", "content": prompt},
            {"role": "user", "content": text},
        ],
        response_format={"type": "json_object"},
        temperature=0,
    )

    raw = json.loads(response.choices[0].message.content)

    return {
        "label": str(raw["label"]),
        "confidence": float(raw["ai_probability"]),
        "reasoning": str(raw["reasoning"]),
    }


# ---------------------------------------------------------------------------
# Signal 2: Stylometric heuristics
# ---------------------------------------------------------------------------

def compute_stylometric(text: str) -> dict:
    """
    Signal 2: Stylometric heuristics (pure Python, no API).
    Measures lexical, syntactic, and punctuation patterns.

    Exploits (from planning.md):
        AI  — low variance, high regularity
        Human — higher stylistic entropy (variation)

    Returns:
        score    (float) raw 0.0 = human-like variation, 1.0 = AI-like regularity
        features (dict)  raw measurements before aggregation
    """
    sentences = [s.strip() for s in re.split(r'[.!?]+', text) if s.strip()]
    words = re.findall(r'\b\w+\b', text.lower())

    if len(sentences) < 2 or len(words) < 20:
        return {
            "score": 0.5,
            "features": {"note": "insufficient text for reliable stylometric measurement"},
        }

    # --- Raw features ---
    sent_lengths = [len(re.findall(r'\b\w+\b', s)) for s in sentences]
    avg_len = sum(sent_lengths) / len(sent_lengths)
    variance = sum((l - avg_len) ** 2 for l in sent_lengths) / len(sent_lengths)

    ttr = len(set(words)) / len(words)

    punct_count = len(re.findall(r'[^\w\s]', text))
    punct_density = punct_count / len(words)

    func_ratio = sum(1 for w in words if w in _FUNCTION_WORDS) / len(words)

    # --- Feature → AI-regularity score (0=human, 1=AI) ---
    # Sentence length variance:
    #   variance < 5  → uniform → AI; variance > 80 → varied → human
    variance_score = max(0.0, min(1.0, 1.0 - variance / 80.0))

    # Type-Token Ratio: skip for texts < 80 words (TTR is unreliable on short
    # texts — high TTR regardless of authorship masks the signal).
    if len(words) >= 80:
        ttr_score = max(0.0, min(1.0, (0.72 - ttr) / 0.32))
        raw_score = max(0.0, min(1.0, 0.55 * variance_score + 0.45 * ttr_score))
    else:
        ttr_score = None
        raw_score = variance_score

    return {
        "score": round(raw_score, 4),
        "features": {
            "avg_sentence_length": round(avg_len, 2),
            "sentence_length_variance": round(variance, 2),
            "type_token_ratio": round(ttr, 4),
            "ttr_used": ttr_score is not None,
            "punctuation_density": round(punct_density, 4),
            "function_word_ratio": round(func_ratio, 4),
        },
    }


# ---------------------------------------------------------------------------
# Signal 3: Predictability — phrase repetition + sentence opener diversity
# ---------------------------------------------------------------------------

def compute_predictability(text: str) -> dict:
    """
    Signal 3: Predictability (pure Python, no API).
    Captures formulaic phrasing and structural repetition that stylometric
    (word-level statistics) and the LLM (semantic) do not directly measure.

    Two sub-measures:
      Bigram uniqueness — ratio of distinct adjacent word pairs to total pairs.
        AI text reuses transition phrases ("it is important", "in conclusion");
        human text produces more varied adjacent-word combinations.
      Sentence opener diversity — fraction of sentences that begin with a
        distinct first word. AI tends to cycle through formal openers
        ("The", "This", "In", "It"); human writing varies more idiosyncratically.

    Calibration (suggested — moderate compression similar to stylometric):
        Pred_score_cal = 0.85 * raw + 0.075

    Returns:
        score    (float) 0.0 = diverse/human-like, 1.0 = formulaic/AI-like
        features (dict)  raw measurements
    """
    sentences = [s.strip() for s in re.split(r'[.!?]+', text) if s.strip()]
    words = re.findall(r'\b\w+\b', text.lower())

    if len(sentences) < 2 or len(words) < 15:
        return {"score": 0.5, "features": {"note": "insufficient text for predictability measurement"}}

    # Bigram uniqueness — phrase-level diversity (distinct from word-level TTR)
    bigrams = list(zip(words, words[1:]))
    unique_bigram_ratio = len(set(bigrams)) / len(bigrams)
    bigram_score = max(0.0, min(1.0, 1.0 - unique_bigram_ratio))

    # Sentence opener diversity
    starters = []
    for s in sentences:
        sw = re.findall(r'\b\w+\b', s.lower())
        if sw:
            starters.append(sw[0])
    starter_diversity = len(set(starters)) / len(starters) if starters else 1.0
    starter_score = max(0.0, min(1.0, 1.0 - starter_diversity))

    raw_score = max(0.0, min(1.0, 0.5 * bigram_score + 0.5 * starter_score))

    return {
        "score": round(raw_score, 4),
        "features": {
            "unique_bigram_ratio": round(unique_bigram_ratio, 4),
            "sentence_starter_diversity": round(starter_diversity, 4),
        },
    }


# ---------------------------------------------------------------------------
# Confidence scoring — ensemble of all three signals
# ---------------------------------------------------------------------------

def combine_signals(llm_raw: float, styl_raw: float,
                    pred_raw: float = 0.5, content_type: str = "text") -> dict:
    """
    Calibrate each signal then combine into a single ensemble confidence score.

    Calibration (compress raw outputs to a shared probability scale):
        LLM:  cal = 0.7  * raw + 0.15   (from planning.md)
        Styl: cal = 0.9  * raw + 0.05   (from planning.md)
        Pred: cal = 0.85 * raw + 0.075  (suggested for Signal 3)

    Weights (see SIGNAL_WEIGHTS — vary by content_type):
        text:              55% LLM + 25% Stylometry + 20% Predictability
        image_description: 70% LLM + 15% Stylometry + 15% Predictability

    Uncertainty: standard deviation of the three calibrated scores.
    More principled than abs(a - b) with 3+ signals — captures spread across
    all three rather than just the largest pairwise gap.

    Confidence bands:
        < 0.15  -> "high"     (signals largely agree)
        < 0.30  -> "moderate" (some disagreement)
        >= 0.30 -> "low"      (signals conflict — treat output with caution)
    """
    llm_cal  = round(0.7  * llm_raw  + 0.15,  4)
    styl_cal = round(0.9  * styl_raw + 0.05,  4)
    pred_cal = round(0.85 * pred_raw + 0.075, 4)

    weights = SIGNAL_WEIGHTS.get(content_type, SIGNAL_WEIGHTS["text"])
    ai_probability = round(
        weights["llm"]            * llm_cal +
        weights["stylometry"]     * styl_cal +
        weights["predictability"] * pred_cal,
        4,
    )

    mean = ai_probability
    variance = sum((s - mean) ** 2 for s in [llm_cal, styl_cal, pred_cal]) / 3
    uncertainty = round(math.sqrt(variance), 4)

    if uncertainty < 0.15:
        confidence_band = "high"
    elif uncertainty < 0.30:
        confidence_band = "moderate"
    else:
        confidence_band = "low"

    return {
        "ai_probability": ai_probability,
        "uncertainty": uncertainty,
        "confidence_band": confidence_band,
        "signals": {
            "llm": llm_cal,
            "stylometry": styl_cal,
            "predictability": pred_cal,
        },
    }


# ---------------------------------------------------------------------------
# Transparency label
# ---------------------------------------------------------------------------

def generate_label(ai_probability: float) -> dict:
    """
    Maps a combined confidence score to the correct transparency label variant.
    Label text and descriptions match planning.md exactly.

    Thresholds (planning.md defines the three variants, not the cutoffs):
        >= 0.65 -> high-confidence AI
        <= 0.35 -> high-confidence human
        between -> uncertain
    Asymmetry is intentional: a false AI accusation harms a creator more than
    a missed detection harms the platform.
    """
    if ai_probability >= 0.65:
        return {
            "label": "Likely AI-generated (high confidence)",
            "description": (
                "Using this text shows patterns of statistical and stylistic "
                "most likely associated with AI-generated text."
            ),
        }
    if ai_probability <= 0.35:
        return {
            "label": "Likely human-written (high confidence)",
            "description": (
                "Using this content shows variations of language and stylistic patterns "
                "consistent with human writing."
            ),
        }
    return {
        "label": "Uncertain — requires review",
        "description": (
            "Using this content shows the signals could not confidently determine "
            "whether this content was AI-generated or human-written based on available signals."
        ),
    }


# ---------------------------------------------------------------------------
# Lookup helpers (shared by appeals, certificates, and status)
# ---------------------------------------------------------------------------

def lookup_submission(content_id: str):
    """Return the original submission audit entry for content_id, or None."""
    if not LOG_PATH.exists():
        return None
    for line in LOG_PATH.read_text().splitlines():
        if not line.strip():
            continue
        entry = json.loads(line)
        if entry.get("content_id") == content_id and "type" not in entry:
            return entry
    return None


def lookup_appeal(content_id: str):
    """Return the most recent appeal entry for content_id, or None."""
    if not LOG_PATH.exists():
        return None
    match = None
    for line in LOG_PATH.read_text().splitlines():
        if not line.strip():
            continue
        entry = json.loads(line)
        if entry.get("type") == "appeal" and entry.get("content_id") == content_id:
            match = entry
    return match


def write_appeal_entry(appeal_id, content_id, creator_id, creator_reasoning, origin):
    entry = {
        "type": "appeal",
        "appeal_id": appeal_id,
        "timestamp": datetime.now(timezone.utc).isoformat(),
        "content_id": content_id,
        "creator_id": creator_id,
        "status": "under_review",
        "creator_reasoning": creator_reasoning,
        "origin_attribution": origin.get("attribution"),
        "origin_ai_probability": origin.get("ai_probability"),
        "origin_llm_score_raw": origin.get("llm_score_raw"),
        "origin_styl_score_raw": origin.get("styl_score_raw"),
    }
    with LOG_PATH.open("a") as f:
        f.write(json.dumps(entry) + "\n")
    return entry


def lookup_certificate(content_id: str):
    """Return the most recent provenance certificate for content_id, or None."""
    if not CERT_PATH.exists():
        return None
    match = None
    for line in CERT_PATH.read_text().splitlines():
        if not line.strip():
            continue
        entry = json.loads(line)
        if entry.get("content_id") == content_id:
            match = entry
    return match


def issue_certificate(content_id: str, creator_id: str, scoring: dict) -> dict:
    cert = {
        "certificate_id": f"cert-{uuid.uuid4().hex[:12]}",
        "content_id": content_id,
        "creator_id": creator_id,
        "issued_at": datetime.now(timezone.utc).isoformat(),
        "ai_probability": scoring["ai_probability"],
        "confidence_band": scoring["confidence_band"],
        "status": "verified_human",
        "badge": "Verified Human-Written",
    }
    with CERT_PATH.open("a") as f:
        f.write(json.dumps(cert) + "\n")
    return cert


