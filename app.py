@app.route("/certificate", methods=["POST"])
def certificate():
    """
    Issue a 'Verified Human-Written' provenance certificate.

    Eligibility: the content must have been classified as
    'Likely human-written (high confidence)' (ai_probability <= 0.35).
    If a certificate already exists for this content_id, the existing
    one is returned (idempotent).

    Display: the returned 'badge' field ("Verified Human-Written") is
    intended to be shown on-platform alongside the content. The
    certificate_id serves as a tamper-evident reference — any downstream
    system can call GET /certificate/<content_id> to verify it.
    """
    data = request.get_json(force=True)
    content_id = data.get("content_id")
    creator_id = data.get("creator_id")

    missing = [f for f, v in [("content_id", content_id), ("creator_id", creator_id)] if not v]
    if missing:
        return jsonify({"error": f"Missing required fields: {', '.join(missing)}"}), 400

    submission = lookup_submission(content_id)
    if submission is None:
        return jsonify({"error": f"No submission found for content_id: {content_id}"}), 404

    ai_prob = submission.get("ai_probability", 1.0)
    if ai_prob > 0.35:
        return jsonify({
            "error": (
                "Certificate not issued. Content must be classified as "
                "'Likely human-written (high confidence)' (ai_probability <= 0.35) "
                "to receive a provenance certificate."
            ),
            "ai_probability": ai_prob,
            "attribution": submission.get("attribution"),
        }), 422

    existing = lookup_certificate(content_id)
    if existing:
        return jsonify(existing), 200

    cert = issue_certificate(content_id, creator_id, {
        "ai_probability": submission["ai_probability"],
        "confidence_band": submission["confidence_band"],
    })
    return jsonify(cert), 201


@app.route("/certificate/<content_id>", methods=["GET"])
def get_certificate(content_id):
    """Retrieve a previously issued provenance certificate by content_id."""
    cert = lookup_certificate(content_id)
    if cert is None:
        return jsonify({"error": f"No certificate found for content_id: {content_id}"}), 404
    return jsonify(cert)


