# Ensemble weights per content type.
# image_description down-weights stylometry because captions are short and
# structurally constrained regardless of authorship, making TTR and sentence
# variance unreliable signals.
SIGNAL_WEIGHTS = {
    "text":              {"llm": 0.55, "stylometry": 0.25, "predictability": 0.20},
    "image_description": {"llm": 0.70, "stylometry": 0.15, "predictability": 0.15},
}


