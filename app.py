@app.route("/appeal", methods=["POST"])
def appeal():
    data = request.get_json(force=True)
    content_id        = data.get("content_id")
    creator_id        = data.get("creator_id")
    creator_reasoning = data.get("creator_reasoning")

    missing = [f for f, v in [
        ("content_id", content_id),
        ("creator_id", creator_id),
        ("creator_reasoning", creator_reasoning),
    ] if not v]
    if missing:
        return jsonify({"error": f"Missing required fields: {', '.join(missing)}"}), 400

    origin = lookup_submission(content_id)
    if origin is None:
        return jsonify({"error": f"No submission found for content_id: {content_id}"}), 404

    appeal_id = str(uuid.uuid4())
    entry = write_appeal_entry(appeal_id, content_id, creator_id, creator_reasoning, origin)

    return jsonify({
        "message": "Appeal received. Your content has been flagged for human review.",
        "appeal_id": appeal_id,
        "content_id": content_id,
        "creator_id": creator_id,
        "status": "under_review",
        "timestamp": entry["timestamp"],
        "original_decision": entry["origin_attribution"],
        "creator_reasoning": creator_reasoning,
    }), 201


@app.route("/status/<content_id>", methods=["GET"])
def status(content_id):
    submission = lookup_submission(content_id)
    if submission is None:
        return jsonify({"error": f"No submission found for content_id: {content_id}"}), 404

    app_entry = lookup_appeal(content_id)

    return jsonify({
        "content_id": content_id,
        "status": app_entry["status"] if app_entry else "submitted",
        "attribution": submission.get("attribution"),
        "ai_probability": submission.get("ai_probability"),
        "submitted_at": submission.get("timestamp"),
        "appeal": {
            "appeal_id": app_entry["appeal_id"],
            "creator_reasoning": app_entry["creator_reasoning"],
            "appealed_at": app_entry["timestamp"],
        } if app_entry else None,
    })


